const changeDir = (dir)=>{
	document.documentElement.setAttribute('dir', dir);
	dir == 'rtl' ? document.documentElement.classList.add('rtl-dir') : document.documentElement.classList.remove('rtl-dir')

	let swip_toolsExist = document.querySelector('.swip-tools')
	if(swip_toolsExist){
		let swip_tools = new Swiper('.swip-tools')
		swip_tools.destroy(true, true);
		
		setTimeout(()=>{
			swip_tools = new Swiper('.swip-tools', {
				slidesPerView: 2,
				spaceBetween: 0,
				direction: 'horizontal',
				rtl: document.documentElement.getAttribute('dir') == 'rtl' ? 'rtl' : 'ltr',
				watchSlidesProgress: true,
				watchOverflow: true,
				loop: false,
				breakpoints: {
					1400: {
						slidesPerView: 6
					},
					1200: {
						slidesPerView: 5
					},
					991: {
						slidesPerView: 4
					},
					576: {
						slidesPerView: 3
					},
				},
				
				pagination: {
					el: '.swiper-pagination',
					clickable: true
				},
			});
		}, 100)
		
	}else{
		let swip_content = new Swiper('.swip-content')
		swip_content.destroy(true, true)
		
		setTimeout(()=>{
			swip_content = new Swiper('.swip-content', {
				slidesPerView: 1,
				spaceBetween: 0,
				direction: 'horizontal',
				rtl: document.documentElement.getAttribute('dir') == 'rtl' ? 'rtl' : 'ltr',
				watchSlidesProgress: true,
				watchOverflow: true,
				loop: false,
				
				pagination: {
					el: '.swiper-pagination',
					clickable: true
				},
			});
		}, 100)

	}
	
}

