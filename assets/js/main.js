const lang_btn = document.querySelector('li.change-lang-btn');
console.log(lang_btn)
if(lang_btn){
	lang_btn.addEventListener('click', ()=>{
		if(localStorage.getItem('lang') == 'ar'){
			localStorage.setItem('lang', 'en')
			changeBootstrapCDN('en')
			changeDir('ltr')
			lang_btn.textContent = 'AR'
			changeTx('en')
			
		}else if(localStorage.getItem('lang') == 'en'){
			localStorage.setItem('lang', 'ar')
			changeBootstrapCDN('ar')
			changeDir('rtl')
			lang_btn.textContent = 'EN'
			changeTx('ar')
			
		}else{
			localStorage.setItem('lang', 'en')
			changeBootstrapCDN('en')
			changeDir('ltr')
			lang_btn.textContent = 'AR'
			changeTx('en')
		}
	})
}

